// 2741. N 찍기
// https://www.acmicpc.net/problem/2741

#include <iostream>

using namespace std;

int main()
{
    int N;
    scanf("%d", &N);

    for (int i = 1; i <= N; ++i)
    {
        printf("%d\n", i);
    }

    return 0;
}


